def convertPTMinSequence2modX(msgfp_sequence):
    my_sequence=msgfp_sequence
    if msgfp_sequence[1]=='.':
        my_sequence=my_sequence[2:]
    if msgfp_sequence[-2]=='.':
        my_sequence=my_sequence[:-2]
    if '(' in my_sequence:
        return my_sequence

    regex=r"[+-]?\d+(?:\.\d+)?"    
    lookup_table={'+15.995':'(Oxidation)','+57.021':'(Carbamidomethyl)','+42.011':'(Acetyl)-',\
                  '-17.027':'(Gln->pyro-Glu)','+79.966':'(Phospho)','-131.040':'(Met-loss)'}
    
    if '-' in my_sequence or '+' in my_sequence:
        ptms=re.findall(regex, my_sequence)
        for j in range(len(ptms)-1,-1,-1):
            ptm=ptms[j]
            idx=my_sequence.index(ptm)
            if idx!=0:
                my_sequence=my_sequence[::-1].replace(ptm[::-1], lookup_table[ptm][::-1],1)[::-1]
            else:
                if '-' in ptm:
                    my_sequence=my_sequence.replace(msgfp_sequence[:len(ptm)],lookup_table[ptm])
                else:
                    my_sequence=my_sequence.replace(ptm,lookup_table[ptm])

    return my_sequence
